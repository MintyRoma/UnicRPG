    /**
     * Функция генерации прилагательного для предмета крафта
     * @param WomanForm Требуется ли женский род прилагательного
     * @return string прилагательное
     */
    public static String GetAdjective(boolean WomanForm)
    {
        Random rnd = new Random();
        String adjective = NameDictionary.adjective.get(rnd.nextInt(0, NameDictionary.adjective.size()));
        if (WomanForm)
        {
            adjective = adjective.substring(0,adjective.length()-2)+"ая";
        }
        adjective = adjective.substring(0,1).toUpperCase()+adjective.substring(1);
        return adjective;
    }

    /**
     * Генерация существительного для описания инструмента
     * @param type тип оружия по WeaponType
     * @return string с существительным
     */
    public static String GetWeaponNoun(WeaponType type)
    {
        Random rnd = new Random();
        switch (type)
        {
            case sword:
                return NameDictionary.SwordNouns.get(rnd.nextInt(0, NameDictionary.SwordNouns.size()));
            case pickaxe:
                return NameDictionary.PickaxeNouns.get(rnd.nextInt(0, NameDictionary.PickaxeNouns.size()));
            case axe:
                return NameDictionary.AxeNouns.get(rnd.nextInt(0, NameDictionary.AxeNouns.size()));
            case hoe:
                return NameDictionary.HoeNouns.get(rnd.nextInt(0, NameDictionary.HoeNouns.size()));
            case shovel:
                return NameDictionary.ShovelNouns.get(rnd.nextInt(0, NameDictionary.ShovelNouns.size()));
        }
        return "";
    }

