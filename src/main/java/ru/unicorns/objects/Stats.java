package ru.unicorns.objects;

/**
 * Класс статов игрока
 */
public class Stats {

    /**
     * Никнейм игрока
     */
    public String Nickname;

    /**
     * Количество скрафченных инструментов
     */
    public int WeaponCrafting = 0;
    /**
     * Количество скрафченной брони
     */
    public int ArmorCrafting = 0;
    /**
     * Количество убийств
     */
    public int Kills = 0;
    /**
     * Количество пройденных блоков
     */
    public int Runer = 0;
    /**
     * Количество скрафченной еды
     */
    public int FoodCrafting = 0;
    /**
     * Количество скрафченных зелий
     */
    public int PotionCrafting = 0;
}
